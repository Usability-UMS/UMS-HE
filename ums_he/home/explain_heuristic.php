<legend>12 HEURISTIC EXPLANATION</legend>
<p><u><b>1) Compatibility</b></u><br/>
The way the system looks and works should be compatible with user conventions and expectations.<br/></p>
<p><u><b>2) Consistency</b></u><br/>
The way the system looks and works should be consistent at all times.</p>
<p><u><b>3) Error Prevention & Correction</b></u><br/>
The system should be design to minimize the possibility of user error, with inbuilt facilities for detecting and handling those which do occur; users should be able to check their inputs and to correct errors or potential error situations before the input is processed.</p>
<p><u><b>4) Explicitness</b></u><br/>
The way the system should works and is structured should be clear to the user.</p>
<p><u><b>5) Flexibility & Control</b></u><br/>
The interface should be sufficiently flexible in structure, in the way information is presented and in terms of what the user can do, to suit the needs and requirements of all users, and to allow them to feel in control of the system.</p>
<p><u><b>6) Functionality</b></u><br/>
The system should meet the needs and requirements of users when carrying out tasks.</p>
<p><u><b>7) Informative Feedback</b></u><br/>
The system should always keep user informed about what is going on through appropriate feedback within reasonable time.</p>
<p><u><b>8) Language & Content</b></u><br/>
The information conveyed should be understandable to the targeted users of the application.</p>
<p><u><b>9) Navigation</b></u><br/>
The system navigation should be structured in a way that allows a user to access support for a specific goal as quickly as possible.</p>
<p><u><b>10) Privacy</b></u><br/>
The system should help the user to protect personal or private information belonging to the user or their clients.</p>
<p><u><b>11) User Guidance & Support</b></u><br/>
Informative, easy-to-use and relevant guidance and support should be provided to help user understand and use the system.</p>
<p><u><b>12) Visual Clarity</b></u><br/>
Information displayed on the screen should be clear, well-organized, unambiguous and easy to read.</p>
